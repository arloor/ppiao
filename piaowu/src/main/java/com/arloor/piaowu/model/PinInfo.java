package com.arloor.piaowu.model;

public class PinInfo {
    private int pin;
    private int pinIndex;

    public int getPin() {
        return pin;
    }

    public void setPin(int pin) {
        this.pin = pin;
    }

    public int getPinIndex() {
        return pinIndex;
    }

    public void setPinIndex(int pinIndex) {
        this.pinIndex = pinIndex;
    }
}
